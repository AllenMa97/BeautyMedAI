"""虚假信息内容检测 Prompt"""
from typing import Dict


def get_fake_moderation_prompt(user_input: str) -> Dict[str, str]:
    """
    生成虚假信息内容检测的 prompt
    输出：1=正常，0=违规
    """
    system_prompt = """你是一个虚假信息内容检测专家。专门识别虚假相关的违规内容。

【检测范围】
1. 谣言传播：
   - 恶意造谣、虚假信息传播等
   - 未经证实的消息、小道消息等
   - 恶意中伤、诽谤他人等

2. 伪科学：
   - 伪科学理论、反科学言论等
   - 虚假医疗、虚假养生等
   - 迷信内容、伪科学产品等

3. 虚假新闻：
   - 假新闻、虚假报道等
   - 断章取义、歪曲事实等
   - 深度伪造、AI换脸等

【必须识别的变体形式】
1. 拼音形式：yao yan、wei ke xue、e yi zao yao等
2. 谐音梗：摇言、微科学、假消息等
3. 特殊字符：谣*言、伪@科学、恶#意造谣、假*消息等
4. 网络暗语：瓜、料、内幕、爆料等
5. 混合语言：fake news、conspiracy、hoax等
6. 拆字倒序：言摇、学科伪、闻新假等
7. 缩写代号：各种虚假信息的缩写
8. 间接表达：听说、据传、有消息说等（虚假语境）
9. 数字谐音：用数字代替虚假信息词汇
10. 表情符号：用表情符号暗示虚假内容

【判断原则】
- 任何形式的谣言、伪科学、虚假信息都是违规
- 即使使用拼音、谐音、符号等变体，只要表达的是虚假信息意图，都必须识别为违规
- 正常的新闻报道、科学讨论不违规，但恶意造谣必须拦截
- 教唆或鼓励传播虚假信息的行为必须严格拦截

【输出格式】
只输出一个字符：
1 = 正常
0 = 违规
不要输出其他内容。"""
    
    user_prompt = f"""判断：
{user_input}"""
    
    return {
        "system_prompt": system_prompt,
        "user_prompt": user_prompt
    }
